  <script>
    // Gallery thumbnail navigation (uses 'active' class)
    function changeMainImage(thumbnailElement) {
      const mainImage = document.getElementById('mainImage');
      const imageUrl = thumbnailElement.getAttribute('data-image-url') || thumbnailElement.src;
      if (!mainImage || !imageUrl) return;
      mainImage.src = imageUrl;

      // Update active thumbnail styling
      document.querySelectorAll('.thumbnail-img').forEach(img => img.classList.remove('active'));
      thumbnailElement.classList.add('active');
    }

    // Keyboard navigation for gallery
    document.addEventListener('keydown', function(e) {
      const thumbnails = Array.from(document.querySelectorAll('.thumbnail-img'));
      if (thumbnails.length === 0) return;
      const activeIndex = thumbnails.findIndex(img => img.classList.contains('active'));

      if (e.key === 'ArrowUp' || e.key === 'ArrowLeft') {
        if (activeIndex > 0) changeMainImage(thumbnails[activeIndex - 1]);
      } else if (e.key === 'ArrowDown' || e.key === 'ArrowRight') {
        if (activeIndex < thumbnails.length - 1) changeMainImage(thumbnails[activeIndex + 1]);
      }
    });

    // view event already recorded by global script for modal links; send explicit view here
    (function() {
      try {
        var fd = new FormData();
        fd.append('product_id', <?= $prod_id ?>);
        fd.append('event', 'view');
        fetch('admin/track_event.php', {
          method: 'POST',
          body: fd
        }).catch(function() {});
      } catch (e) {}
    })();

    // Toggle product description between short/long versions
    document.addEventListener('DOMContentLoaded', function() {
      var btn = document.getElementById('btnToggleDesc');
      if (!btn) return;

      btn.addEventListener('click', function(e) {
        e.preventDefault();
        var shortDiv = document.getElementById('descShort');
        var fullDiv  = document.getElementById('descFull');
        if (!fullDiv || !shortDiv) return;

        if (fullDiv.style.display === 'none') {
          fullDiv.style.display = '';
          shortDiv.style.display = 'none';
          btn.textContent = 'Sembunyikan';
        } else {
          fullDiv.style.display = 'none';
          shortDiv.style.display = '';
          btn.textContent = 'Tampilkan selengkapnya';
        }
      });
    });
  </script>

