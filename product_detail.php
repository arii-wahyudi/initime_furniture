            <div id="descShort"><?= nl2br($desc_raw) ?></div>
          <?php endif; ?>
        </div>

        <div class="mt-4 d-flex gap-2">
          <a href="https://wa.me/<?= $wa_number ?>?text=Halo%20saya%20ingin%20memesan%20<?= urlencode($product['nama_produk']) ?>" class="btn btn-success wa-link ms-auto" data-product-id="<?= $prod_id ?>">Pesan Via WhatsApp</a>
          <a href="product.php" class="btn btn-outline-secondary">Kembali</a>
        </div>

        <?php if (!empty($related)): ?>
          <div class="mt-4">
            <h5 class="mb-3">Produk Terkait</h5>
            <div class="row g-3">
              <?php foreach ($related as $rp):
                $rimg = $rp['gambar'] ?? '';
                $rprice = isset($rp['harga']) ? number_format((float)$rp['harga'], 0, ',', '.') : '-';
              ?>
                <div class="col-6 col-md-3">
                  <a href="product_detail.php?slug=<?= urlencode($rp['slug']) ?>" class="text-decoration-none text-dark">
                    <div class="card h-100 shadow-sm">
                      <img src="<?= htmlspecialchars(public_image_url($rimg ?? '')) ?>" class="card-img-top object-fit-cover" style="height:120px;" alt="<?= htmlspecialchars($rp['nama_produk']) ?>">
                      <div class="card-body p-2">
                        <div class="small fw-bold"><?= htmlspecialchars($rp['nama_produk']) ?></div>
                        <div class="small text-success">Rp <?= $rprice ?></div>
                      </div>
                    </div>
                  </a>
                </div>
              <?php endforeach; ?>
            </div>
          </div>
        <?php endif; ?>

      </div>
    </div>
